    public void InsertarProducto(){
    
        Producto prod = new Producto();
    
        if(!"----".equals(cmbCategoria.getSelectedItem())){   
            String codigo = txtCodigo.getText();
            String nombre = txtNombrePro.getText();
            String descr = txpDescripcion.getText();

            float precioCos = Float.parseFloat(txtPrecioCosto.getText());
            float precioVen = Float.parseFloat(txtPrecioVenta.getText());

            int indice = cmbCategoria.getSelectedIndex();
            int idCat = categorias.get(indice).getIdCategoria();

            int canti = Integer.parseInt(txtCantidad.getText());
            int min = Integer.parseInt(txtMinimo.getText());

            //Insertar Producto
            prod.setCodigoBarra(codigo);
            prod.setNombreProducto(nombre);
            prod.setDescripcion(descr);
            prod.setPrecioCosto(precioCos);
            prod.setPrecioVenta(precioVen);
            prod.setIdCategoria(idCat);
            prod.setCantidadInv(canti);
            prod.setCantidadMin(min);

            boolean resultado = this.crud.insertar(SQL.insertarProducto(prod));
            if (resultado) {
                JOptionPane.showMessageDialog(null, "Producto insertado correctamente");
                buscar(true,"","",tablaActualizar); //mostramos los datos actualizados en la tabla con el metodo mostrar
                buscar(true,"","", tablaInsertar);
                buscar(true,"","", tablaEliminar);
                buscar(true, "", "", tablaActualizarStock);
                
                txtCodigo.setText("");
                txtNombrePro.setText("");
                txtPrecioCosto.setText("");
                txtPrecioVenta.setText("");
                txtCantidad.setText("");
                txtMinimo.setText("");
                txpDescripcion.setText("");

            }else{
                JOptionPane.showMessageDialog(null, "Producto no insertado ");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecciona un departamento");
        }
    }//Cierra insertar Producto
     
    
    
